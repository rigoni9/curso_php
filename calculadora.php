<?php

/**
 * operadores (+ - * /)
*/

$x = 8;
$y = 10;
$operador = "*";

if ($operador == "+") {
    $soma = $x + $y;
    echo "A função é $x + $y = $soma";
} else if ($operador == "-") {
    echo "A função é $x - $y = " . ($x - $y);
} else if ($operador == "*") {
    echo "A função é $x * $y = " . ($x * $y);
} else if ($operador == "/") {
    echo "A função é $x / $y = " . ($x / $y);
} 
